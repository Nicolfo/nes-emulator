pub fn set_pixel(frame: &mut [u8], x: i32, y: i32, color: [u8; 3]) {
    if x < 0 || y < 0 || x >= WIDTH as i32 || y >= HEIGHT as i32 {
        return;
    }
    let i = (y as usize * WIDTH + x as usize) * 4;
    frame[i] = color[0];
    frame[i + 1] = color[1];
    frame[i + 2] = color[2];
    frame[i + 3] = 0xFF;
}

pub fn fill_rect(frame: &mut [u8], x: i32, y: i32, w: i32, h: i32, color: [u8; 3]) {
    for dy in 0..h {
        for dx in 0..w {
            set_pixel(frame, x + dx, y + dy, color);
        }
    }
}

pub fn clear(frame: &mut [u8], color: [u8; 3]) {
    for px in frame.chunks_exact_mut(4) {
        px[0] = color[0];
        px[1] = color[1];
        px[2] = color[2];
        px[3] = 0xFF;
    }
}

pub fn draw_text(frame: &mut [u8], x: i32, y: i32, text: &str, color: [u8; 3], scale: i32) {
    let mut cx = x;
    for ch in text.chars() {
        let c = ch.to_ascii_uppercase();
        let idx = (c as usize).wrapping_sub(0x20);
        if idx < 64 {
            let glyph = &FONT[idx];
            for (row, bits) in glyph.iter().enumerate() {
                for col in 0..8 {
                    if bits & (1 << col) != 0 {
                        fill_rect(frame, cx + col * scale, y + row as i32 * scale, scale, scale, color);
                    }
                }
            }
        }
        cx += 8 * scale;
    }
}

pub fn text_width(text: &str, scale: i32) -> i32 {
    text.chars().count() as i32 * 8 * scale
}

pub fn draw_text_centered(frame: &mut [u8], y: i32, text: &str, color: [u8; 3], scale: i32) {
    let x = (WIDTH as i32 - text_width(text, scale)) / 2;
    draw_text(frame, x, y, text, color, scale);
}

/// 16x16 monochrome icon; bit 15 of each row = leftmost pixel.
pub fn draw_icon(frame: &mut [u8], x: i32, y: i32, icon: &[u16; 16], color: [u8; 3]) {
    for (row, bits) in icon.iter().enumerate() {
        for col in 0..16 {
            if bits & (0x8000 >> col) != 0 {
                set_pixel(frame, x + col, y + row as i32, color);
            }
        }
    }
}
